    static void escribirArchivo(String nombreDeArchivo, String texto) {
        try {
            String carpeta = "";
            carpeta += CarpetaGuardada;
            FileWriter fw = new FileWriter(carpeta + "/" + nombreDeArchivo + ".txt");
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(texto);
            bw.flush();
        } catch (Exception e) {
        }
    }

    static String leerArchivo(String Nombre) {
        String archivo = "";
        String texto = "";
        archivo += CarpetaGuardada + "/" + Nombre + ".txt";
        try {
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            texto = br.readLine();
        } catch (Exception e) {
        }
        return texto;
    }
